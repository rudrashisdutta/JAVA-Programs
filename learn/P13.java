public class P13 {
    public static void main(String[] args) {
        fun(2 | 5 | 3);
    }

    static void fun(int x) {
        if (x == 2) {
            System.out.println("qwerty");
        }
        if (x == 5) {
            System.out.println("qqqqqqwertyyyyy");
        }
        if (x == 3) {
            System.out.println("qwertyyyyy");
        }
    }
}
